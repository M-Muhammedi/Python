# Liste oluşturma
meyveler = ["elma", "muz", "çilek"]
print(meyveler)

# Listeye öğe ekleme
meyveler.append("portakal")
print(meyveler)

# Listeden öğe çıkarma
meyveler.remove("muz")
print(meyveler)

# Liste öğelerine erişme

print(meyveler[0])  # İlk öğe

print(meyveler[-1]) # Son öğe

print(meyveler[0:2]) # ilk iki öğe

print(len(meyveler)) # listenin eleman sayısını gösterir

